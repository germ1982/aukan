    /**
     * Lists all Sds_com_barrio models.
     * @return mixed
     */
    public function actionIndex()
    {
        $searchModel = new Sds_com_barrioSearch();
        $dataProvider = $searchModel->search(Yii::$app->request->queryParams);
        Mds_sys_log::guardarLog(Mds_sys_log::ACCION_CONSULTA, 'sds_com_barrio', null, array());
