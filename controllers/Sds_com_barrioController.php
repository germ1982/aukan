     */
    protected function findModel($id)
    {
        if (($model = Sds_com_barrio::findOne($id)) !== null) {
            return $model;
        } else {
            throw new NotFoundHttpException('The requested page does not exist.');
        }
    }

    public function actionCmb_barrio($id)
    {
        $barrios = Sds_com_barrio::find()
            ->where(['idlocalidad' => $id])->orderBy(['nombre' => SORT_ASC])
            ->all();
        $cmbBarrio = "";
        if (sizeof($barrios) > 0) {
            foreach ($barrios as $barrio) {
                $cmbBarrio = $cmbBarrio . "<option value='" . $barrio->idbarrio . "'>" . $barrio->nombre . "</option>";
            }
        }

        return $cmbBarrio;
    }

